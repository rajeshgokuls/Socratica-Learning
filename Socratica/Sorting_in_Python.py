#sorting alphabetically

earth_metals = ["Beryllium", "Magnesium", "Calcium", "Strontium",
                "Barium","Radium"]

earth_metals.sort()
print(earth_metals) #sort alphabetically

earth_metals.sort(reverse=True)#names are sorted in reverse alphabetical order
print(earth_metals)

